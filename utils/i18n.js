const LANGUAGE_KEY = 'hud_language';
let currentLanguage = 'ar';
const listeners = new Set();

export const loadLanguage = () => {
  try {
    const saved = globalThis.localStorage?.getItem(LANGUAGE_KEY);
    if (saved && LANGUAGES[saved]) currentLanguage = saved;
  } catch (error) { console.warn('[i18n] load failed', error); }
  return currentLanguage;
};

export const saveLanguage = (language) => {
  if (!LANGUAGES[language]) return false;
  currentLanguage = language;
  try { globalThis.localStorage?.setItem(LANGUAGE_KEY, language); } catch (error) { console.warn('[i18n] save failed', error); }
  return true;
};

export const t = (key, replacements = {}) => {
  const source = LANGUAGES[currentLanguage]?.translations?.[key] ?? LANGUAGES.ar?.translations?.[key] ?? key;
  return Object.entries(replacements).reduce(
    (text, [name, value]) => text.replace(new RegExp(`\{${name}\}`, 'g'), String(value)),
    source
  );
};

export const hasTranslation = (key, language = currentLanguage) => Boolean(
  key && (Object.hasOwn(LANGUAGES[language]?.translations || {}, key)
    || Object.hasOwn(LANGUAGES.ar?.translations || {}, key))
);

export const translateMessage = (messageOrKey, replacements = {}) => {
  if (messageOrKey && typeof messageOrKey === 'object') {
    const key = messageOrKey.key || messageOrKey.messageKey;
    if (key && hasTranslation(key)) return t(key, messageOrKey.replacements || replacements);
    return String(messageOrKey.message || messageOrKey.fallback || '');
  }
  const value = String(messageOrKey ?? '');
  return hasTranslation(value) ? t(value, replacements) : value;
};

export const getCurrentLanguage = () => currentLanguage;
export const getLanguageInfo = (code = currentLanguage) => LANGUAGES[code] || null;
export const getAvailableLanguages = () => Object.values(LANGUAGES).map((language) => ({
  code: language.code, name: language.name, flag: language.flag, nativeName: language.nativeName, dir: language.dir
}));

export const applyLanguage = (root = globalThis.document) => {
  if (!root) return currentLanguage;
  const language = getLanguageInfo();
  const html = root.documentElement || root.ownerDocument?.documentElement;
  if (html) { html.lang = language.code; html.dir = language.dir; html.dataset.lang = language.code; }
  root.querySelectorAll?.('[data-i18n]').forEach((element) => { element.textContent = t(element.dataset.i18n); });
  root.querySelectorAll?.('[data-i18n-html]').forEach((element) => { element.textContent = t(element.dataset.i18nHtml); });
  for (const attribute of ['placeholder', 'title', 'alt']) {
    root.querySelectorAll?.(`[data-i18n-${attribute}]`).forEach((element) => {
      element.setAttribute(attribute, t(element.dataset[`i18n${attribute[0].toUpperCase()}${attribute.slice(1)}`]));
    });
  }
  listeners.forEach((listener) => listener(currentLanguage));
  return currentLanguage;
};

export const createLanguageDropdown = (container = globalThis.document?.getElementById('languageDropdownContainer')) => {
  if (!container) return null;
  container.replaceChildren();
  const wrapper = document.createElement('div'); wrapper.className = 'lang-dropdown-wrapper';
  const trigger = document.createElement('button'); trigger.type = 'button'; trigger.className = 'lang-main-btn'; trigger.id = 'mainLanguageBtn';
  const menu = document.createElement('div'); menu.className = 'lang-dropdown'; menu.id = 'languageDropdown';
  const refreshTrigger = () => { const info = getLanguageInfo(); trigger.textContent = `${info.flag} ${info.name} ▼`; };
  refreshTrigger();
  trigger.addEventListener('click', () => menu.classList.toggle('open'));
  getAvailableLanguages().forEach((language) => {
    const button = document.createElement('button'); button.type = 'button'; button.className = 'lang-option'; button.dataset.lang = language.code;
    button.textContent = `${language.flag} ${language.name} — ${language.nativeName}`;
    button.addEventListener('click', () => { switchLanguage(language.code); menu.classList.remove('open'); refreshTrigger(); });
    menu.append(button);
  });
  wrapper.append(trigger, menu); container.append(wrapper);
  return wrapper;
};

export const switchLanguage = (language) => {
  if (!saveLanguage(language)) return false;
  applyLanguage();
  return true;
};

export const subscribeLanguage = (listener) => { listeners.add(listener); return () => listeners.delete(listener); };
export const initTranslationSystem = () => { loadLanguage(); createLanguageDropdown(); applyLanguage(); return currentLanguage; };

export default {
  LANGUAGES, t, hasTranslation, translateMessage, getCurrentLanguage, getLanguageInfo,
  getAvailableLanguages, applyLanguage, switchLanguage, loadLanguage, saveLanguage,
  initTranslationSystem, createLanguageDropdown, subscribeLanguage
};
